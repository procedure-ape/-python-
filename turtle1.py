# 乌龟绘图
import turtle

p=turtle
p.pensize
p.color("blue")
p.circle(60,360)
p.pu()
p.goto(60,0)
p.pd()
p.color('black')
p.circle(30,360)
p.pu()
p.goto(120,0)
p.pd()
p.color("red")
p.circle(30,260)
p.pu()
p.goto(90,-30)
p.pd()
p.color("green")
p.circle(30.60)
p.pd()
p.color("yellow")
p.circlr(30.60)

p.done()